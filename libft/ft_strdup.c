#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*s2;
	size_t	l;
	size_t	i;

	l = ft_strlen((char *)s1);
	i = 0;
	s2 = (char *)malloc(l + 1);
	if (!s2)
		return (NULL);
	while (s1[i] != '\0')
	{
		s2[i] = s1[i];
		i++;
	}
	s2[i] = '\0';
	return (s2);
}
